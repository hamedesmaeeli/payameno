package registration.data;

public class IdentifierChar {
    private int id;
    private String name;
    public IdentifierChar() {
        
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
